			<div class="page-content">
				<p class="regular-para"><strong><span class="yellow-span">MAIN IDEA: </span></strong>Communication is a skill. Therefore, it is learnable, it can be practiced, and it can improve. Every couple faces communication challenges. Maybe you do some things that aren’t healthy, and you know your spouse does things that aren’t healthy. But we want you to take each of these as a “couple problem.” Both of you own the problem, and both of you own the solution.</p>

				<h2 class="five-challenging">THE TOP FIVE CHALLENGES COUPLES FACE:</h2>

				<p class="regular-para"><strong><span class="yellow-span">1. KEEPING COMMUNICATION OPEN</span></strong> Creating a space of trust is key! You need to be on the same page: that you both have the best intentions for each other and that you’re on the same team. You both know that you have each other’s backs.</p>

				<p class="regular-para"><strong><span class="yellow-span">2. FEELING HEARD</span></strong> If you or your partner is Hungry, on Edge, Angry, or Tired, your conversation is going to get HEATED pretty quickly - it probably isn’t going to be a good time for certain conversations, especially if it’s about why those feelings are happening. Assess the timing, then listen to understand, not to respond.</p>

				<p class="regular-para"><strong><span class="yellow-span">3. OVERCOMING MISUNDERSTANDINGS</span></strong> Good communication happens when what you hear is what the speaker meant to say. But every normal marriage has moments when miscommunications happen.</p>

				<div class="one-third-sec">
					<div class="one-sec"></div>
					<div class="third-sec">
						
						<p class="regular-para"><strong><span class="yellow-span">4. HAVING DEEP CONVERSATIONS</span></strong> When a lack of depth is an issue, it’s usually rooted in intimacy, not in communication. But by practicing great communication through deep conversations, you can boost your intimacy, too!</p>

						<p class="regular-para"><strong><span class="yellow-span">5. CREATING TIME TO TALK</span></strong> The number one obstacle to creating time to talk is having an overly-full schedule. Priorities come into play here, but oftentimes, there isn’t anything you can drop off the schedule. So then what? That’s when intention comes into play.</p>

						<p class="regular-para">No matter which challenge you experience most (or if you feel all ﬁve equally!), we know you have what it takes to overcome them. If at any point you and your spouse feel stuck, don’t hesitate to contact us. We can help you assess the level of help you may need to overcome the challenges you’re facing in your marriage.</p>
