				<p class="regular-para">Your score on this scale indicates whether your natural strength is to be more optimistic or realistic in how you process information. If you plot on the optimistic side of the scale, it is predictable that when processing new information you will be more trusting and accepting. If you plot on the realistic side of the scale, you will be more skeptical and want to validate information before trusting. Again, each side of the scale represents a strength in processing information.</p>

				<h2 class="yellow-heading">HANDLING CHANGE</h2>
				<p class="regular-para">Your score on this scale indicates whether your natural strength is to be more accepting or resistant in how you handle change. If you are on the accepting side of this scale, you will tend to be fast-paced. You are comfortable managing many projects simultaneously, and you allow change to drive your plan. If you plot on the resistant side of the scale, you will lean toward a slower pace. You prefer to focus on one project at a time, and you tend to want to plan for change. Handling change effectively requires a combination of these strengths.</p>

